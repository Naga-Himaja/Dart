void main()
{
  String grade = 'A';
  if(grade == 'A')
  {
    print("Very good!");
  }
  else if(grade=='B')
  {
    print("Good!");
  }
  else if(grade=='C')
  {
    print("Average!");
  }
  else if(grade=='D' || grade=='F')
  {
    print("Fail");
  }
  else
  {
    print("Invalid grade!");
  }
}
